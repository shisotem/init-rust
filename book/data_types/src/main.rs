fn main() {
    // numeric literal
    println!("98_222: {}", 98_222);
    println!("0xff: {}", 0xff);
    println!("0o77: {}", 0o77);
    println!("0b1111_0000: {}", 0b1111_0000);
    println!("b'A': {}", b'A');
    println!("b'B': {}", b'B');
    // 98_222: 98222
    // 0xff: 255
    // 0o77: 63
    // 0b1111_0000: 240
    // b'A': 65
    // b'B': 66
}
